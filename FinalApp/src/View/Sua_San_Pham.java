    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        String tensp = TenSP.getText();
        if(tensp == null){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập Tên Sản Phẩm!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        String loai = (String) cb_loai.getSelectedItem();
        
        int giasp, sl;
        if(gia.getText().trim().isEmpty()){
            giasp = 0;
        }else{
            giasp = Integer.parseInt(gia.getText());
        }
        
        if(soluong.getText().trim().isEmpty()){
            sl = 0;
        }else{
            sl = Integer.parseInt(soluong.getText());
        }
        
        SanPham SP = new SanPham(this.MaSP,tensp, loai, giasp, sl);
        
        boolean updated = SanPhamDAO.EditSP(SP);
        if(updated){
            JOptionPane.showMessageDialog(null, "Thông tin sản phẩm đã được cập nhập", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            for (int i = 0; i < tableModel.getRowCount(); i++) {
                int maSP = (int) tableModel.getValueAt(i, 0); 
                if (maSP == this.MaSP) {
                    // Cập nhật thông tin cho hàng tương ứng
                    tableModel.setValueAt(tensp, i, 1);
                    tableModel.setValueAt(loai, i, 2);
                    tableModel.setValueAt(giasp, i, 3);
                    tableModel.setValueAt(sl, i, 4);
                    break;
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cập Nhập Thất Bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        dispose();
    }//GEN-LAST:event_btn_suaActionPerformed

