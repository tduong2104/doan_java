    public void EditSPShow(){
        SanPham SP =SanPhamDAO.ShowSPbyMa(this.MaSP);
        TenSP.setText(SP.getTenSP());
        gia.setText(String.valueOf(SP.getGia()));
        soluong.setText(String.valueOf(SP.getSoLuong()));
        
        cb_loai.setSelectedItem(SP.getLoaiSP());
    }
    public void DSDMSP() {
        List<Object[]> dataList = SanPhamDAO.ListDMSP(); 
        List<String> DSDMSP_name = new ArrayList<>();
        for (Object[] data : dataList) {
            if (data.length > 1 && data[1] != null) {
                DSDMSP_name.add((String) data[1]);
            }
        }

        // Sử dụng phương thức toArray(T[] a) với kiểu mảng cụ thể
        String[] list = DSDMSP_name.toArray(new String[0]);

        DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>(list);
        cb_loai.setModel(comboBoxModel);
    }
